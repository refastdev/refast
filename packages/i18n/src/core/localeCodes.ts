export type CodeType = {
  native: string;
  english: string;
};
export type CodeMap = {
  [key: string]: CodeType;
};
export const codes: CodeMap = (() => {
  const lines = csv.split('\n');
  if (lines.length < 0) return {};
  const result: CodeMap = {};
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    if (line == null) continue;
    const currentLine = line.split(',');
    if (currentLine.length < 3) continue;
    result[currentLine[0]!] = {
      native: currentLine[1]!,
      english: currentLine[2]!,
    };
  }
  return result;
})();
